const RCON = new Uint32Array([
  0x00000000, 0x01000000, 0x02000000, 0x04000000, 0x08000000, 0x10000000, 0x20000000, 0x40000000, 0x80000000, 0x1b000000,
  0x36000000,
]);

export interface AesKeySchedule {
  Nk: number;
  Nr: number;
  roundKeys: Uint8Array;
  invRoundKeys: Uint8Array;
}

const rotWord = (w: number): number => ((w << 8) | (w >>> 24)) >>> 0;

const subWord = (w: number): number =>
  ((SBOX[(w >>> 24) & 0xff] << 24) | (SBOX[(w >>> 16) & 0xff] << 16) | (SBOX[(w >>> 8) & 0xff] << 8) | SBOX[w & 0xff]) >>> 0;

const gfMul = (a: number, b: number): number => {
  let res = 0;
  let aa = a;
  let bb = b;
  for (let i = 0; i < 8; i += 1) {
    if (bb & 1) res ^= aa;
    const hiBit = aa & 0x80;
    aa = (aa << 1) & 0xff;
    if (hiBit) aa ^= 0x1b;
    bb >>>= 1;
  }
  return res;
};

const wordToBytes = (w: number, out: Uint8Array, offset: number) => {
  out[offset] = (w >>> 24) & 0xff;
  out[offset + 1] = (w >>> 16) & 0xff;
  out[offset + 2] = (w >>> 8) & 0xff;
  out[offset + 3] = w & 0xff;
};

const bytesToWord = (bytes: Uint8Array, offset: number): number =>
  ((bytes[offset] << 24) | (bytes[offset + 1] << 16) | (bytes[offset + 2] << 8) | bytes[offset + 3]) >>> 0;

export const createAesKey = (key: Uint8Array): AesKeySchedule => {
  const keyLength = key.length;
  if (keyLength !== 16 && keyLength !== 24 && keyLength !== 32) {
    throw new Error('AES key must be 128, 192, or 256 bits');
  }

  const Nk = keyLength / 4;
  const Nr = Nk + 6;
  const Nb = 4;
  const totalWords = Nb * (Nr + 1);

  const w = new Uint32Array(totalWords);
  for (let i = 0; i < Nk; i += 1) {
    w[i] = bytesToWord(key, i * 4);
  }

  for (let i = Nk; i < totalWords; i += 1) {
    let temp = w[i - 1];
    if (i % Nk === 0) {
      temp = subWord(rotWord(temp)) ^ RCON[i / Nk];
    } else if (Nk > 6 && i % Nk === 4) {
      temp = subWord(temp);
    }
    w[i] = (w[i - Nk] ^ temp) >>> 0;
  }

  const roundKeys = new Uint8Array((Nr + 1) * BLOCK_SIZE_BYTES);
  for (let i = 0; i < totalWords; i += 1) {
    wordToBytes(w[i], roundKeys, i * 4);
  }

  const invRoundKeys = new Uint8Array(roundKeys);

  return { Nk, Nr, roundKeys, invRoundKeys };
};

const subBytes = (state: Uint8Array) => {
  for (let i = 0; i < state.length; i += 1) {
    state[i] = SBOX[state[i]];
  }
};

const invSubBytes = (state: Uint8Array) => {
  for (let i = 0; i < state.length; i += 1) {
    state[i] = INV_SBOX[state[i]];
  }
};

const shiftRows = (s: Uint8Array) => {
  const t1 = s[1];
  s[1] = s[5];
  s[5] = s[9];
  s[9] = s[13];
  s[13] = t1;

  const t2 = s[2];
  const t6 = s[6];
  s[2] = s[10];
  s[6] = s[14];
  s[10] = t2;
  s[14] = t6;

  const t3 = s[3];
  s[3] = s[15];
  s[15] = s[11];
  s[11] = s[7];
  s[7] = t3;
};

const invShiftRows = (s: Uint8Array) => {
  const t1 = s[13];
  s[13] = s[9];
  s[9] = s[5];
  s[5] = s[1];
  s[1] = t1;

  const t2 = s[2];
  const t6 = s[6];
  s[2] = s[10];
  s[6] = s[14];
  s[10] = t2;
  s[14] = t6;

  const t3 = s[3];
  s[3] = s[7];
  s[7] = s[11];
  s[11] = s[15];
  s[15] = t3;
};

const mixColumns = (s: Uint8Array) => {
  for (let c = 0; c < 4; c += 1) {
    const col = c * 4;
    const b0 = s[col];
    const b1 = s[col + 1];
    const b2 = s[col + 2];
    const b3 = s[col + 3];
    s[col] = gfMul(b0, 2) ^ gfMul(b1, 3) ^ b2 ^ b3;
    s[col + 1] = b0 ^ gfMul(b1, 2) ^ gfMul(b2, 3) ^ b3;
    s[col + 2] = b0 ^ b1 ^ gfMul(b2, 2) ^ gfMul(b3, 3);
    s[col + 3] = gfMul(b0, 3) ^ b1 ^ b2 ^ gfMul(b3, 2);
  }
};

const invMixColumns = (s: Uint8Array) => {
  for (let c = 0; c < 4; c += 1) {
    const col = c * 4;
    const b0 = s[col];
    const b1 = s[col + 1];
    const b2 = s[col + 2];
    const b3 = s[col + 3];
    s[col] = gfMul(b0, 0x0e) ^ gfMul(b1, 0x0b) ^ gfMul(b2, 0x0d) ^ gfMul(b3, 0x09);
    s[col + 1] = gfMul(b0, 0x09) ^ gfMul(b1, 0x0e) ^ gfMul(b2, 0x0b) ^ gfMul(b3, 0x0d);
    s[col + 2] = gfMul(b0, 0x0d) ^ gfMul(b1, 0x09) ^ gfMul(b2, 0x0e) ^ gfMul(b3, 0x0b);
    s[col + 3] = gfMul(b0, 0x0b) ^ gfMul(b1, 0x0d) ^ gfMul(b2, 0x09) ^ gfMul(b3, 0x0e);
  }
};

const addRoundKey = (state: Uint8Array, roundKeys: Uint8Array, round: number) => {
  const offset = round * BLOCK_SIZE_BYTES;
  for (let i = 0; i < BLOCK_SIZE_BYTES; i += 1) {
    state[i] ^= roundKeys[offset + i];
  }
};

export const encryptBlock = (block: Uint8Array, schedule: AesKeySchedule): Uint8Array => {
  if (block.length !== BLOCK_SIZE_BYTES) {
    throw new Error('AES block must be 16 bytes');
  }
  const state = new Uint8Array(block);
  addRoundKey(state, schedule.roundKeys, 0);

  for (let round = 1; round < schedule.Nr; round += 1) {
    subBytes(state);
    shiftRows(state);
    mixColumns(state);
    addRoundKey(state, schedule.roundKeys, round);
  }

  subBytes(state);
  shiftRows(state);
  addRoundKey(state, schedule.roundKeys, schedule.Nr);
  return state;
};

export const decryptBlock = (block: Uint8Array, schedule: AesKeySchedule): Uint8Array => {
  if (block.length !== BLOCK_SIZE_BYTES) {
    throw new Error('AES block must be 16 bytes');
  }
  const state = new Uint8Array(block);
  addRoundKey(state, schedule.invRoundKeys, schedule.Nr);

  for (let round = schedule.Nr - 1; round > 0; round -= 1) {
    invShiftRows(state);
    invSubBytes(state);
    addRoundKey(state, schedule.invRoundKeys, round);
    invMixColumns(state);
  }

  invShiftRows(state);
  invSubBytes(state);
  addRoundKey(state, schedule.invRoundKeys, 0);
  return state;
};
